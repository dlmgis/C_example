/*------------------------------------------------
【程序设计】
--------------------------------------------------
输入一些整数，编程计算并输出其中所有正数的和，
输入负数时不累加，继续输入下一个数。
输入0时，表示输入数据结束。
用do…while实现,并用到continue语句。
------------------------------------------------*/
#include <stdio.h>
void main()
{
        int n,sum=0;
         /**********Program**********/
        do{
			scanf("%d",&n);
			if(n>0)
			sum+=n;
		}while(n);
         /********** End **********/
        printf("sum=%d\n",sum);
}
